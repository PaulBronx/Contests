void ip_info (unsigned ip){
    if (ip & 0x80000000 == 0) { //class A
        printf("A : %u - %u\n", ip >> 24 , ip & 0x00FFFFFF);
    } else if(ip & 0x40000000 == 0) {// class B
        printf("B : %u - %u\n", (ip >> 16) & 0x00003FFF , ip & 0x0000FFFF);
    } else if(ip & 0x20000000 == 0) {// class C
        printf("C : %u - %u\n", (ip >> 8) & 0x001FFFFF , ip & 0x000000FF);
    }
}
